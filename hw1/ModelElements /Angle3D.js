class Angle3D {
  constructor() {
    this.angleX = 0;
    this.angleY = 0;
    this.angleZ = 0;
  }

  getAngleX() {
    return this.angleX;
  }

  setAngleX(angleX) {
    this.angleX = angleX;
  }

  getAngleY() {
    return this.angleY;
  }

  setAngleY(angleY) {
    this.angleY = angleY;
  }

  getAngleZ() {
    return this.angleZ;
  }

  setAngleZ(angleZ) {
    this.angleZ = angleZ;
  }
}
